const SPRITES: Record<string, number[][]> = {
  pixie_S_0: S0, pixie_S_1: S1,
  pixie_N_0: N0, pixie_N_1: N1,
  pixie_E_0: E0, pixie_E_1: E1,
}

function mirrorH(pixels: number[][]): number[][] {
  return pixels.map(row => [...row].reverse())
}

export function generatePixieTextures(scene: Phaser.Scene): void {
  const allSprites: Record<string, number[][]> = { ...SPRITES }
  allSprites['pixie_W_0'] = mirrorH(E0)
  allSprites['pixie_W_1'] = mirrorH(E1)

  for (const [key, pixels] of Object.entries(allSprites)) {
    const h = pixels.length
    const w = Math.max(...pixels.map(r => r.length))
    const g = scene.add.graphics()

    for (let y = 0; y < h; y++) {
      for (let x = 0; x < (pixels[y]?.length ?? 0); x++) {
        const idx = pixels[y][x]
        if (idx === 0) continue
        const color = PALETTE[idx]
        if (color === null || color === undefined) continue
        const alpha = (idx === 5 || idx === 6) ? WING_ALPHA : 1
        g.fillStyle(color, alpha)
        g.fillRect(x, y, 1, 1)
      }
    }

    g.generateTexture(key, w, h)
    g.destroy()
  }

  for (const dir of ['S', 'N', 'E', 'W']) {
    scene.anims.create({
      key: `pixie_${dir}`,
      frames: [
        { key: `pixie_${dir}_0` },
        { key: `pixie_${dir}_1` },
      ],
      frameRate: 5,
      repeat: -1,
    })
  }
}

export type PixieDir = 'S' | 'N' | 'E' | 'W'

export function facingFromDelta(dcol: number, drow: number): PixieDir {
  if (dcol > 0 && drow >= 0) return 'E'
  if (dcol <= 0 && drow > 0) return 'S'
  if (dcol < 0 && drow <= 0) return 'W'
  return 'N'
}
